#operadores_aritmeticos.ps1. Script donde aparezcan todas las operaciones aritméticas vistas.
#Autor: Otero

Clear-Host

$x=50
$y=25

Write-Host "Dados los números " $x " y " $y ":"

$Suma= $x+$y

Write-Host "El resultado de sumar X e Y es "$Suma

$Resta= $x-$y

Write-Host "El resultado de restar X e Y es "$Resta

$Multiplicacion= $x*$y

Write-Host "El resultado de multiplicar X e Y es "$Multiplicacion

$Division= $x/$y

Write-Host "El resultado de dividir X e Y es "$Division

$Resto= $x%$y

Write-Host "El resto de dividir X e Y es "$Resto
